# solution after tips (I solved this task earlier. Leetcode display solution automatically. Result sequence similar Fibonacci seq)
class Solution:
    def climbStairs(self, n) -> int:
        return round((0.5 + math.sqrt(5) / 2) ** (n + 1) / math.sqrt(5))


